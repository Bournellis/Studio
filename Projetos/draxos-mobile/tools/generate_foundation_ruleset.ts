type SourceKind = "definition_json" | "tool_model" | "battle_simulator";
type CanonicalizationMode = "json" | "text";

interface SourceSpec {
  path: string;
  kind: SourceKind;
  canonicalization: CanonicalizationMode;
}

interface SourceDigest {
  path: string;
  kind: SourceKind;
  sha256: string;
  canonical_bytes: number;
}

const PROJECT_ROOT = new URL("../", import.meta.url);
const RULESET_ID = "foundation_ruleset_v0";
const RULESET_PATH = new URL(
  "data/rulesets/foundation_ruleset_v0.json",
  PROJECT_ROOT,
);
const OUTPUT_PATHS = [
  new URL("server/functions/_shared/foundation_ruleset.ts", PROJECT_ROOT),
  new URL("supabase/functions/_shared/foundation_ruleset.ts", PROJECT_ROOT),
];

