  "arena_buffs.json",
  "arena_rewards.json",
  "base_structures.json",
  "battle_fixtures.json",
  "bot_builds.json",
  "crafting_recipes.json",
  "passives.json",
  "pets.json",
  "potions.json",
  "power_bands.json",
  "pve_arena_difficulties.json",
  "pve_arenas.json",
  "pve_enemies.json",
  "rewards.json",
